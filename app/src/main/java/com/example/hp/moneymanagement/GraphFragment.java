    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        pieChart = (PieChart) getActivity().findViewById(R.id.PieChart);
        pieChart.setRotationEnabled(true);
        pieChart.setHoleRadius(25f);
        pieChart.setTransparentCircleAlpha(0);
        pieChart.setCenterText("Your Account");
        pieChart.setCenterTextSize(10);
        pieChart.setDrawEntryLabels(true);

        ArrayList<PieEntry> yEntry = new ArrayList<PieEntry>();
        int salary=0;
        int expanse=0;
        for(int i=0;i<Data.values.size();i++){
            if(Data.category.get(i).compareTo("Salary")==0){
                salary+=Data.values.get(i);
            }
            else{
                expanse+=Data.values.get(i);
            }
        }
        yEntry.add(new PieEntry(salary,"income"));
        yEntry.add(new PieEntry(expanse,"expense"));

        PieDataSet pieDataSet = new PieDataSet(yEntry, "Expenses and income");
        pieDataSet.setSliceSpace(2);
        pieDataSet.setValueTextSize(24);
        pieDataSet.setValueTextColor(Color.rgb(65,163,245));

        ArrayList<Integer> colors = new ArrayList<Integer>();
        colors.add(Color.WHITE);
        colors.add(Color.BLACK);
        pieDataSet.setColors(colors);

        Legend legend = pieChart.getLegend();
        legend.setForm(Legend.LegendForm.CIRCLE);
        legend.setPosition(Legend.LegendPosition.LEFT_OF_CHART);

        PieData pieData = new PieData(pieDataSet);
        pieChart.setData(pieData);
        pieChart.invalidate();


        drawerLayout =(DrawerLayout) getActivity().findViewById(R.id.drawer_layout);
        ImageButton imageButton = (ImageButton) getActivity().findViewById(R.id.drawer);
        imageButton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                drawerLayout.openDrawer(Gravity.LEFT);
            }
        });

    }
